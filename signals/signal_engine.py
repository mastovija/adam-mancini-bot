async def generar_señal_llm(precio_es, nivel, tipo_nivel, direccion,
                      today, bars_15, fb_info=None, tweets=None,
                      criterio_ventana='') -> dict:
    """
    Asks Claude Haiku whether Adam would enter in this situation.

    FIX C-13 — async:
    client.messages.create() is synchronous and takes 1-3 s. Inside async
    it blocks ib_insync's event loop: no tick can be processed.
    asyncio.to_thread() runs the call in an OS thread and returns
    control to the event loop without stopping any other process.
    """
    client = anthropic.Anthropic(api_key=ANTHROPIC_API_KEY)

    content_plan = (
        today.get('content_plan') or
        today.get('setup', '') + '\n' + today.get('invalida_si', '') or
        'Plan not available'
    )
    hora_est, _, _ = get_trading_window()

    if bars_15:
        v    = bars_15[-1]
        m    = SPY_TO_ES_MULTIPLIER
        open_15  = f"{v['open']  * m:.1f}"
        high_15  = f"{v['high']  * m:.1f}"
        low_15   = f"{v['low']   * m:.1f}"
        close_15 = f"{v['close'] * m:.1f}"
        confirmacion = ("YES — bullish candle confirms"
                        if confirmar_con_vela_15min(bars_15, nivel, direccion)
                        else "NO — candle does not confirm")
    else:
        open_15 = high_15 = low_15 = close_15 = f"{precio_es:.1f}"
        confirmacion = "No 15min candle data"

    user_prompt = SIGNAL_USER_PROMPT.format(
        content_plan     = content_plan,
        tweets_hoy       = formatear_tweets_para_prompt(tweets or []),
        hora_est         = hora_est,
        precio_es        = f"{precio_es:.1f}",
        nivel            = f"{nivel:.1f}",
        tipo_nivel       = tipo_nivel,
        bias             = today.get('bias', 'unknown'),
        criterio_ventana = criterio_ventana,
        fb_descripcion   = (fb_info or {}).get('descripcion', '⚠️ No analysis'),
        open_15          = open_15,
        high_15          = high_15,
        low_15           = low_15,
        close_15         = close_15,
        confirmacion     = confirmacion,
    )

    try:
        # C-13: to_thread frees ib_insync's event loop during the LLM call.
        # Phase 2: the DECISION runs on Sonnet 5 (LLM_DECISION_MODEL) with
        # adaptive thinking for sharper, more Mancini-faithful judgment. The
        # methodology rubric is a CACHED system block (Phase 1) → ~0.1x on repeat
        # calls. max_tokens is generous because the total output includes the
        # thinking tokens plus the reasoning `razon` + the closed JSON.
        response = await asyncio.to_thread(
            client.messages.create,
            model      = LLM_DECISION_MODEL,
            max_tokens = 2500,
            thinking   = {"type": "adaptive", "display": "summarized"},
            output_config = {"effort": "low"},
            system     = [{"type": "text", "text": MANCINI_SYSTEM,
                           "cache_control": {"type": "ephemeral"}}],
            messages   = [{"role": "user", "content": user_prompt}],
        )
        if response.stop_reason == 'max_tokens':
            print("  ⚠️  LLM hit max_tokens — response may be truncated")
        # Sonnet 5 returns thinking blocks before the answer — pull the TEXT
        # block, not content[0] (which may be a thinking block).
        text_block = next(
            (b.text for b in response.content if getattr(b, 'type', None) == 'text'),
            None,
        )
        if text_block is None:
            raise ValueError("No text block in the LLM response")
        raw = text_block.strip()
        # Fix JSON parsing: the LLM sometimes adds text before/after the JSON
        # causing 'Extra data' in json.loads. Extracting between the first '{'
        # and the last '}' ignores markdown and extra text.
        inicio = raw.find('{')
        fin    = raw.rfind('}')
        if inicio != -1 and fin != -1 and fin > inicio:
            raw = raw[inicio:fin + 1]
        else:
            raise ValueError(f"No JSON found in the LLM response: {raw[:200]}")
        resultado = json.loads(raw)

        # Mathematical validation: stop < entry for longs
        if resultado.get('entrar'):
            entrada = resultado.get('entrada_es')
            stop    = resultado.get('stop_es')
            if entrada and stop and float(stop) >= float(entrada):
                resultado['entrar'] = False
                resultado['razon']  = f"Discarded: stop ({stop}) >= entry ({entrada}) in LONG"

        return resultado
    except Exception as e:
        print(f"  ❌ LLM error: {e}")
        return {"entrar": False, "razon": f"Error: {e}"}


