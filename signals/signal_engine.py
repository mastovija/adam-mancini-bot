def get_todays_tweets() -> list:
    """
    Reads Adam's tweets from today from TWO sources (monitor + scraper).

    FIX B-8 — timezone:
    It used to use local datetime.now() (Spain, UTC+2 in summer). Any of
    Adam's tweets after 18:00 EST (midnight in Spain) were compared against
    the NEXT day's date in Spain and got excluded, even though Adam is still
    mid-trading-session (until 16:00 EST).

    Now the 'trading day' is anchored to New York time (MARKET_TIMEZONE),
    like the rest of the bot's logic. And the tweets' UTC timestamps are
    converted to NY before comparing the date.
    """
    tz_ny = pytz.timezone(MARKET_TIMEZONE)   # America/New_York
    # 'today' in New York time — aligned with Adam's trading session
    hoy   = datetime.now(tz_ny).strftime('%Y-%m-%d')
    tweets     = []
    ids_vistos = set()

    if TWEETS_FILE.exists():
        try:
            state = json.load(open(TWEETS_FILE, encoding='utf-8'))
            # Note: tweet_monitor.py saves fecha_hoy in local time (Spain).
            # During market hours (07:30-16:00 EST = 13:30-22:00 Spain)
            # both dates coincide, so this comparison is correct.
            # The mismatch would only occur between 22:00 and midnight Spain
            # (16:00-18:00 EST, after-hours), where the tweets are history and
            # the past-words filter would reject them anyway.
            if state.get('fecha_hoy') == hoy:
                for item in state.get('tweets_hoy', []):
                    tweet = item.get('tweet', item) if isinstance(item, dict) else item
                    tid = tweet.get('id')
                    if tweet.get('text') and not tweet.get('is_retweet') and tid:
                        tweets.append(tweet)
                        ids_vistos.add(tid)
        except Exception:
            pass

    raw_file = DATA_DIR / 'raw' / 'tweets' / 'adam_mancini_tweets.json'
    if raw_file.exists():
        try:
            all_tweets = json.load(open(raw_file, encoding='utf-8'))
            for tweet in all_tweets:
                created = tweet.get('created_at', '')
                tid     = tweet.get('id')
                if not created or tweet.get('is_retweet') or tid in ids_vistos:
                    continue
                try:
                    # Parse Twitter's UTC timestamp and convert to NY time
                    # before comparing the date. This way '2026-06-26 22:30 UTC'
                    # (= 18:30 EST, after-close on the 26th) isn't classified as
                    # a tweet from the 27th just because it's already 00:30 on the 27th in Spain.
                    dt_utc = datetime.strptime(created, '%a %b %d %H:%M:%S +0000 %Y')
                    dt_ny  = pytz.utc.localize(dt_utc).astimezone(tz_ny)
                    if dt_ny.strftime('%Y-%m-%d') == hoy:
                        tweets.append(tweet)
                        ids_vistos.add(tid)
                except Exception:
                    pass
        except Exception:
            pass

    def _ts(t):
        try:
            # Sort by UTC timestamp (same basis for all tweets)
            dt_utc = datetime.strptime(t.get('created_at', ''), '%a %b %d %H:%M:%S +0000 %Y')
            return pytz.utc.localize(dt_utc)
        except Exception:
            return datetime.min.replace(tzinfo=pytz.utc)

    return sorted(tweets, key=_ts)


def formatear_tweets_para_prompt(tweets: list) -> str:
    """Format Adam's tweets from today for the LLM prompt."""
    if not tweets:
        return "No tweets from Adam yet today."
    lines = []
    for tweet in tweets[-12:]:
        texto = tweet.get('text', '').strip()
        if not texto:
            continue
        hora = ''
        created = tweet.get('created_at', '')
        if created:
            try:
                dt   = datetime.strptime(created, '%a %b %d %H:%M:%S +0000 %Y')
                hora = f" [{dt.strftime('%H:%M')} UTC]"
            except Exception:
                pass
        lines.append(f"• {texto}{hora}")
    return '\n'.join(lines) if lines else "No tweets from Adam yet today."


