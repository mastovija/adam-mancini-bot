def load_today() -> dict | None:
    """Loads the day map generated by the newsletter parser."""
    if not TODAY_FILE.exists():
        print("  ⚠️  today.json does not exist — run newsletter_parser.py first")
        return None

    with open(TODAY_FILE) as f:
        data = json.load(f)

    if data.get('date'):
        dias = (datetime.now().date() -
                datetime.strptime(data['date'], '%Y-%m-%d').date()).days
        if dias > 2:
            print(f"  ⚠️  today.json is {dias} day(s) old")

    if len(data.get('content_plan', '')) < 500:
        print("  ⚠️  content_plan too short — run newsletter_parser.py --force")

    return data


def get_all_levels(today: dict) -> list:
    """
    Extracts all of the day's levels (supports + resistances + pivot)
    and deduplicates them by numeric value.

    Resistances are included because of Adam's Level Reclaim setup.

    FIX B-5 — deduplication across lists:
    The LLM extractor (processor.py) sometimes classifies the same price as
    both a support AND a resistance (e.g. 7425 in the today.json of June 24).
    Without deduplicating, check_once queries the LLM TWICE for that level
    (once as support, once as resistance) and the prompt lists also mix
    types for the same price, confusing the model.

    CRITERION: if the same level (rounded to an integer) appears several times,
    we keep the FIRST occurrence in order: supports first, then resistances,
    then pivot. The type that 'wins' is the one the LLM extracted first,
    which is usually the most representative.
    """
    niveles = []
    for n in today.get('soportes', []):
        if n:
            niveles.append({'nivel': float(n), 'tipo': 'support'})
    for n in today.get('resistencias', []):
        if n:
            niveles.append({'nivel': float(n), 'tipo': 'resistance'})
    if today.get('nivel_critico'):
        nc = float(today['nivel_critico'])
        if not any(abs(x['nivel'] - nc) < 0.5 for x in niveles):
            niveles.append({'nivel': nc, 'tipo': 'pivot'})

    # Deduplicate by value rounded to the nearest integer.
    # round(7425.0) == round(7425.3) == 7425 → the first entry is kept.
    # This prevents the engine from querying the LLM twice for the same level
    # and the prompt from including contradictory types for the same price.
    vistos: dict[int, dict] = {}
    for entry in niveles:
        clave = round(entry['nivel'])
        if clave not in vistos:
            vistos[clave] = entry

    deduplicados = sorted(vistos.values(), key=lambda x: x['nivel'], reverse=True)

    n_total      = len(niveles)
    n_dedup      = len(deduplicados)
    if n_total != n_dedup:
        print(f"  🔧 B-5: {n_total - n_dedup} duplicate level(s) removed "
              f"({n_total} → {n_dedup})")

    return deduplicados


